namespace Intacct\Functions\Common\Query\Comparison;

abstract class AbstractInteger extends AbstractComparison
{

    /**
     * @var int
     */
    protected $value;

    /**
     * @return int
     */
    public function getValue(): int
    {
        return $this->value;
    }

    /**
     * @param int $value
     */
    public function setValue($value)
    {
        if (!is_int($value)) {
            throw new \InvalidArgumentException('Comparison value variable must be an int type');
        }
        $this->value = $value;
    }
}
